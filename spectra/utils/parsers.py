def parse_ports(port_spec):
    """
    Analisa a especificação de portas e retorna lista de portas.
    
    Formatos suportados:
    - Porta única: '80'
    - Lista: '80,443,22'
    - Range: '1-1024'
    - Combinado: '80,443,1000-2000'
    """
    if not port_spec:
        return []
    
    ports = set()
    
    # Divide por vírgulas para tratar cada parte
    parts = [part.strip() for part in port_spec.split(',')]
    
    for part in parts:
        if not part:
            continue
            
        if '-' in part:
            # Range de portas
            try:
                start, end = map(int, part.split('-', 1))
                if 1 <= start <= end <= 65535:
                    ports.update(range(start, end + 1))
                else:
                    raise ValueError(f"Range de portas inválido: {part}")
            except ValueError as e:
                raise ValueError(f"Erro ao processar range '{part}': {e}")
        else:
            # Porta única
            try:
                port = int(part)
                if 1 <= port <= 65535:
                    ports.add(port)
                else:
                    raise ValueError(f"Porta inválida: {port}")
            except ValueError:
                raise ValueError(f"Porta inválida: {part}")
    
    return sorted(list(ports))

